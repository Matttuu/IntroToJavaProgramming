package ChapterOne.ProgrammingExercises;

public class AreaAndPerimeterOfARectangle {
    public static void main(String[] args) {
        System.out.println("Width of rectangle = 4.5, and height = 7.9");
        System.out.println("Area = width x height = " + 4.5 * 7.9);
        System.out.println("Perimeter = 2 x (height + width) = " + 2 * (4.5 + 7.9));
    }
}
